    private void btnStartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnStartActionPerformed
        // TODO add your handling code here:
        if(txtP1.getText().isEmpty() || txtP2.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"The names are not defined");
        }else{ 
            if(btnStart.getText().equals("Start")){
                btnStart.setText("Restart");
                Random r = new Random();
                if(r.nextBoolean())
                    turn=1;
                else
                    turn=-1;
                paint();
                nTurns = 0;
                btn0.setActionCommand("");btn1.setActionCommand("");btn2.setActionCommand("");
                btn3.setActionCommand("");btn4.setActionCommand("");btn5.setActionCommand("");
                btn6.setActionCommand("");btn7.setActionCommand("");btn8.setActionCommand("");
                btn0.setEnabled(true);btn1.setEnabled(true);btn2.setEnabled(true);
                btn3.setEnabled(true);btn4.setEnabled(true);btn5.setEnabled(true);
                btn6.setEnabled(true);btn7.setEnabled(true);btn8.setEnabled(true);
            }else{
                if(JOptionPane.showConfirmDialog(null, "Are you sure?")==0){
                    btnStart.setText("Start");
                    turn = 0;
                    win = false;
                    btn0.setEnabled(true);btn1.setEnabled(true);btn2.setEnabled(true);
                    btn3.setEnabled(true);btn4.setEnabled(true);btn5.setEnabled(true);
                    btn6.setEnabled(true);btn7.setEnabled(true);btn8.setEnabled(true);
                    txtP1.setBackground(Color.WHITE);
                    txtP2.setBackground(Color.WHITE);
                }
            }
        }
    }//GEN-LAST:event_btnStartActionPerformed

