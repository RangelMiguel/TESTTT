    private void btnPress(JButton btn){
        if(btn.isEnabled() && btnStart.getText().equals("Restart") && win==false){
            btn.setEnabled(false);
            btn.setActionCommand(turn+"");
            try {
                AePlayWave aw;
                URL url = PlayAudio.class.getResource("/res/audio/open.wav");
                aw = new AePlayWave(url);
                aw.start();
             } catch (URISyntaxException ex) {
                Logger.getLogger(PlayAudio.class.getName()).log(Level.SEVERE, null, ex);
             }
            if(turn==1){
                btn.setDisabledIcon(new javax.swing.ImageIcon(getClass().getResource("/res/images/xs.jpg")));
            }else{
                btn.setDisabledIcon(new javax.swing.ImageIcon(getClass().getResource("/res/images/os.png")));
            }
            testWin();
        }else{
            try {
                AePlayWave aw;
                URL url = PlayAudio.class.getResource("/res/audio/draw.wav");
                aw = new AePlayWave(url);
                aw.start();
             } catch (URISyntaxException ex) {
                Logger.getLogger(PlayAudio.class.getName()).log(Level.SEVERE, null, ex);
             }
        }
    }
    
    private void testWin(){
        nTurns++;
        if(!btn0.getActionCommand().isEmpty() &&
                btn0.getActionCommand().equals(btn1.getActionCommand()) && btn1.getActionCommand().equals(btn2.getActionCommand()))
            win = true;
        if(!btn3.getActionCommand().isEmpty() &&
                btn3.getActionCommand().equals(btn4.getActionCommand()) && btn4.getActionCommand().equals(btn5.getActionCommand()))
            win = true;
        if(!btn6.getActionCommand().isEmpty() &&
                btn6.getActionCommand().equals(btn7.getActionCommand()) && btn7.getActionCommand().equals(btn8.getActionCommand()))
            win = true;
        if(!btn0.getActionCommand().isEmpty() &&
                btn0.getActionCommand().equals(btn3.getActionCommand()) && btn3.getActionCommand().equals(btn6.getActionCommand()))
            win = true;
        if(!btn1.getActionCommand().isEmpty() &&
                btn1.getActionCommand().equals(btn4.getActionCommand()) && btn4.getActionCommand().equals(btn7.getActionCommand()))
            win = true;
        if(!btn2.getActionCommand().isEmpty() &&
                btn2.getActionCommand().equals(btn5.getActionCommand()) && btn5.getActionCommand().equals(btn8.getActionCommand()))
            win = true;
        if(!btn0.getActionCommand().isEmpty() &&
                btn0.getActionCommand().equals(btn4.getActionCommand()) && btn4.getActionCommand().equals(btn8.getActionCommand()))
            win = true;
        if(!btn2.getActionCommand().isEmpty() &&
                btn2.getActionCommand().equals(btn4.getActionCommand()) && btn4.getActionCommand().equals(btn6.getActionCommand()))
            win = true;
        if(win){
            try {
                AePlayWave aw;
                URL url = PlayAudio.class.getResource("/res/audio/win.wav");
                aw = new AePlayWave(url);
                aw.start();
             } catch (URISyntaxException ex) {
                Logger.getLogger(PlayAudio.class.getName()).log(Level.SEVERE, null, ex);
             }
            JOptionPane.showMessageDialog(null, "The winner is " + (turn==1 ? txtP1.getText() : txtP2.getText()));
        }else{
            if(nTurns==9){
                try {
                AePlayWave aw;
                URL url = PlayAudio.class.getResource("/res/audio/draw.wav");
                aw = new AePlayWave(url);
                aw.start();
             } catch (URISyntaxException ex) {
                Logger.getLogger(PlayAudio.class.getName()).log(Level.SEVERE, null, ex);
             }
                JOptionPane.showMessageDialog(null, "Draw");
            }else{
                turn*=-1;
                paint();
            }
        }
    }
    
    private void paint(){
        if(turn==1){
            txtP1.setBackground(Color.RED);
            txtP2.setBackground(Color.WHITE);
        }else{
            txtP2.setBackground(Color.RED);
            txtP1.setBackground(Color.WHITE);
        }
    }
